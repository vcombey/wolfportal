#include "libft.h"

char	*ft_strrchr(char const *s, int c)
{
	unsigned int	i;

	i = ft_strlen(s);
	if (!s)
		return (NULL);
	while (i > 0)
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i--;
	}
	if (s[0] == (char)c)
		return ((char *)s);
	return (NULL);
}
