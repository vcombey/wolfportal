#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned i, char *))
{
	unsigned int i;

	i = 0;
	if (!s || !f)
		return ;
	while (s[i])
	{
		f(i, &(s[i]));
		i++;
	}
}
